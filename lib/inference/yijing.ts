function getHexagramIndex(year: number, month: number, day: number): number {
  const seed = year + month * 31 + day * 17;
  return Math.abs(seed % 15); // 0-14
}

export function getYijingInfo(year: number, month: number, day: number): YijingResult {
  const index = getHexagramIndex(year, month, day);
  const num = HEXAGRAM_NAMES[index][0];
  const hexagram = HEXAGRAMS[num];
  
  if (!hexagram) {
    return {
      hexagramName: '\u4e7e\u5366',
      hexagramSymbol: '\u2610\u2610',
      interpretation: '\u5929\u884c\u5065\uff0c\u541b\u5b50\u4ee5\u81ea\u5f3a\u4e0d\u606f\u3002',
      advice: '\u6062\u590d\u9700\u8981\u8010\u5fc3\uff0c\u4e0d\u662f\u51b2\u523a\u3002',
    };
  }

  return {
    hexagramName: hexagram.name,
    hexagramSymbol: hexagram.symbol,
    interpretation: hexagram.meaning,
    advice: hexagram.advice,
  };
}
